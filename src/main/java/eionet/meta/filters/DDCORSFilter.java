package eionet.meta.filters;

import java.io.IOException;

import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.FilterConfig;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.commons.lang3.StringUtils;

/**
 * Filter for DataDictionary CORS support. Currently, onyl allows for json output/
 *
 * @author enver
 */
public class DDCORSFilter implements Filter {
    @Override
    public void init(FilterConfig filterConfig) throws ServletException {
    }

    @Override
    public void doFilter(ServletRequest req, ServletResponse resp, FilterChain chain) throws IOException, ServletException {
        HttpServletResponse response = (HttpServletResponse) resp;

        HttpServletRequest request = (HttpServletRequest) req;
        String requestUrl = request.getRequestURL().toString();

        // filter only if request ends with /json
        int lastIndexOfSlash = StringUtils.lastIndexOf(requestUrl, "/");
        String subString = StringUtils.substring(requestUrl, lastIndexOfSlash);
        if (StringUtils.startsWith(subString, "/json") || StringUtils.startsWith(subString, "/csv")) {
            response.setHeader("Access-Control-Allow-Origin", "*");
            response.setHeader("Access-Control-Allow-Methods", "POST, GET, OPTIONS, DELETE");
            response.setHeader("Access-Control-Max-Age", "3600");
            response.setHeader("Access-Control-Allow-Headers", "x-requested-with");
        }

        chain.doFilter(req, resp);
    } // end of method doFilter

    @Override
    public void destroy() {
    }
} // end of class DDCORSFilter
